import turtle
wn=turtle.Screen()
t1=turtle.Turtle()
width=wn.window_width()
x1=0.0 - (width-40)/3
x2=0.0
x3=0.0 + (width-40)/3
def drawTriangleAt(size,pos):
    t1.penup()
    t1.goto(pos,0)
    t1.setheading(0)
    t1.pendown()
    t1.write(t1.pos())
    t1.forward(100)
    t1.write(t1.pos())
    t1.left(120)
    t1.forward(100)
    t1.write(t1.pos())
    t1.left(120)
    t1.forward(100)
    t1.write(t1.pos())
    t1.left(120)
drawTriangleAt(100,x1)
def drawPentagon(size,pos):
    t1.penup()
    t1.goto(pos,0)
    t1.setheading(0)
    t1.pendown()
    t1.write(t1.pos())
    t1.forward(size)
    t1.left(72)
    t1.write(t1.pos())
    t1.forward(size)
    t1.left(72)
    t1.write(t1.pos())
    t1.forward(size)
    t1.left(72)
    t1.write(t1.pos())
    t1.forward(size)
    t1.left(72)
    t1.write(t1.pos())
    t1.forward(size)
drawPentagon(100,x2)
